import { Request, Response } from "express";
import { StoreServices } from "../services";

export class StoreController {
  constructor() {  }

  all = async (req: Request, res: Response) => {
    const { status, message, data } = await StoreServices.getAll();
    return res.status(status).json({
      message,
      data,
    });
  };

  one = async (req: Request, res: Response) => {
    const {id}=req.params
    const { status, message, data } = await StoreServices.getOne(Number(id));
    return res.status(status).json({
      message,
      data,
    });
  };
  create = async (req: Request, res: Response) => {
    const { status, message, data } = await StoreServices.create(req.body);
    return res.status(status).json({
      message,
      data,
    });
  };
  update = async (req: Request, res: Response) => {
    const {id}=req.params
    const { status, message, data } = await StoreServices.update(Number(id),req.body);
    return res.status(status).json({
      message,
      data,
    });
  };

  delete = async (req: Request, res: Response) => {
    const {id}=req.params
    const { status, message, data } = await StoreServices.delete(Number(id));
    return res.status(status).json({
      message,
      data,
    });
  };
}